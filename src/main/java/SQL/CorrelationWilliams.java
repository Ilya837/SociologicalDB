package SQL;

public class CorrelationWilliams {

    String field;
    String num1;
    String num2;
    String num3;
    String num4;

    public CorrelationWilliams(String field, String num1, String num2, String num3, String num4) {
        this.field = field;
        this.num1 = num1;
        this.num2 = num2;
        this.num3 = num3;
        this.num4 = num4;
    }

    public String getField() {
        return field;
    }

    public String getNum1() {
        return num1;
    }

    public String getNum2() {
        return num2;
    }

    public String getNum3() {
        return num3;
    }

    public String getNum4() {
        return num4;
    }
}
